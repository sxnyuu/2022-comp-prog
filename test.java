         int[] testArray = new int[] {2, 3, 4, 5, 6};

        /**  prints out array in reverse 
        
        * @param testArray the original array
        */
        
        public static void printReverse(String[] testArray) {
            for (int i = testArray.length; i > -1; i--) {
                int reverseReturn = 0;
                testArray[i] = reverseReturn;
                System.out.print(reverseReturn);
            }
        }
            
        
             
        
         
        
        
